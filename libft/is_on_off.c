#include "libft.h"

int				is_on(int i, int j)
{
	if ((i & (ON << j)) != 0)
		return (1);
	return (0);
}

int				is_off(int i, int j)
{
	if ((i & (ON << j)) != 0)
		return (0);
	return (1);
}

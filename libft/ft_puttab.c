#include "libft.h"

void	ft_puttab(char **tab)
{
	size_t x;

	if (!(tab) || !(*tab))
		return ;
	x = 0;
	while (tab[x])
	{
		if (tab[x][0] != '\0')
			ft_putendl(tab[x]);
		x++;
	}
}
